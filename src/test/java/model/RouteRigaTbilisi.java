package model;

public class RouteRigaTbilisi {
    private float airDistanceHours;
    private int roadDistanceKm;
    private int railDistanceKm;

    public float getAirDistanceHours() {
        return airDistanceHours;
    }

    public void setAirDistanceHours(float airDistanceHours) {
        this.airDistanceHours = airDistanceHours;
    }

    public int getRoadDistanceKm() {
        return roadDistanceKm;
    }

    public void setRoadDistanceKm(int roadDistanceKm) {
        this.roadDistanceKm = roadDistanceKm;
    }

    public int getRailDistanceKm() {
        return railDistanceKm;
    }

    public void setRailDistanceKm(int railDistanceKm) {
        this.railDistanceKm = railDistanceKm;
    }
}
