package model;

public class Shoes {
    private String typeOfShoes;
    private String color;
    private float size;
    private float heelHeight;
    private int numberOfPairs;
    private String material;
    private String shoesIntended;

    public String getTypeOfShoes() {
        return typeOfShoes;
    }

    public void setTypeOfShoes(String typeOfShoes) {
        this.typeOfShoes = typeOfShoes;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public float getSize() {
        return size;
    }

    public void setSize(float size) {
        this.size = size;
    }

    public float getHeelHeight() {
        return heelHeight;
    }

    public void setHeelHeight(float heelHeight) {
        this.heelHeight = heelHeight;
    }

    public int getNumberOfPairs() {
        return numberOfPairs;
    }

    public void setNumberOfPairs(int numberOfPairs) {
        this.numberOfPairs = numberOfPairs;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getShoesIntended() {
        return shoesIntended;
    }

    public void setShoesIntended(String shoesIntended) {
        this.shoesIntended = shoesIntended;
    }
}
